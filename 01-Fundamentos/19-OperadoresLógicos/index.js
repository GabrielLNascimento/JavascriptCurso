/*
OPERADORES LÓGICOS
&& -> And -> isso E aquilo
|| -> Or -> isso Ou aquilo  
! -> not -> não
*/

// todas as condições precisam ser true
console.log(true && true && true && true)

// se retornar falso, a condição inteiro retorna false
console.log(true && false && true && true)


// a primeira condição que der true, ela retorna true
console.log(false || false || true || false)



