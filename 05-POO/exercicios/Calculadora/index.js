class Calculadora {
    static add(x, y) {
        return x + y
    }

    static sub(x, y) {
        return x - y
    }

    static div(x, y) {
        return x / y
    }

    static mult(x, y) {
        return x * y
    }
}

console.log(Calculadora.add(2, 3))
console.log(Calculadora.sub(2, 3))
console.log(Calculadora.div(2, 3))
console.log(Calculadora.mult(2, 3))